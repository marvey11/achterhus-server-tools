#! /usr/bin/env python3
import shlex
import sys
from pathlib import Path

# Add the project root to sys.path
project_root = Path(__file__).resolve().parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from lib.configuration import load_and_validate_config


def main() -> None:
    json_env_file = project_root / ".env.json"

    # Define the keys we expect to exist in the JSON
    required_keys = [
        "status-dir",
        "photo-inbox",
        "storage-dir",
        "photo-storage",
    ]

    config = load_and_validate_config(json_env_file, required_keys)
    if config is None:
        sys.exit(1)

    try:
        # Mapping: BASH_VAR_NAME -> config_key (resolved)
        # Ensure Path objects are stringified correctly before quoting
        env_config = {
            "SERVICE_STATUS_DIR": str(config.get_path("status-dir")),
            "PHOTO_INBOX": str(config.get_path("photo-inbox")),
            "PHOTO_STORAGE": str(config.get("photo-storage")),
        }
    except KeyError as e:
        print(f"❌ Resolution Error: {e}", file=sys.stderr)
        sys.exit(1)

    env_path = project_root / ".env"

    try:
        with open(env_path, "w", encoding="utf-8") as env_file:
            env_file.write("# GENERATED BY generate_env.py - DO NOT EDIT\n")
            for key, value in env_config.items():
                # shlex.quote handles spaces, quotes, and special characters
                env_file.write(f"{key}={shlex.quote(value)}\n")

        print(f"✅ Created {env_path}")
    except OSError as e:
        print(f"❌ Failed to write .env file: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
